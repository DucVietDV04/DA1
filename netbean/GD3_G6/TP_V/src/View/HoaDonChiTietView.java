    public void fillTableHDCT(String maHD) {
        List<HoaDon> list = hdService.findHoaDonCT(maHD);
        mol = (DefaultTableModel) tblHDCT.getModel();
        mol.setRowCount(0);
        for (HoaDon hoaDon : list) {
            mol.addRow(hoaDon.toDataRowHDCT());
        }
    }

